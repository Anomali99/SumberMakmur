    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        if(validasiIn()){
            String id = tf_id.getText();
            String nama = tf_nama.getText();
            String tlp = tf_tlp.getText();
            String usrname = tf_username.getText();
            String alamat = tf_alamat.getText();
            String level = cb_level.getSelectedItem().toString();
            String pass = pf_pp.getText();
            String imgPath = lb_namaImg.getText();

            model_penjual mod = new model_penjual();

            mod.setId_penjual(id);
            mod.setNama_penjual(nama);
            mod.setUsername(usrname);
            mod.setTlp_penjual(tlp);
            mod.setAlamat_penjual(alamat);
            mod.setLevel(level);
            mod.setPassword(pass);
            mod.setImgPath(imgPath);

            servis.tambahData(mod);
            if(!imgPath.trim().isEmpty())
                servis.ubahFoto(mod);
            
            tblMod.tambahData(mod);
            dispose();
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

