    private void select() {
        JnaFileChooser fileChos = new JnaFileChooser();
        Window window = null;
        boolean action = fileChos.showOpenDialog(window);
        if (action){
            System.out.println(fileChos.getSelectedFile());
            File selectFile = fileChos.getSelectedFile();
            String path = selectFile.getAbsolutePath();
            lb_namaImg.setText(path);
            
            try{
                byte[] img = Files.readAllBytes(selectFile.toPath());
                ImageIcon imgIcon = new ImageIcon(img);

                int lbW = 100;
                int lbH = 100;

                int imgW = imgIcon.getIconWidth();
                int imgH = imgIcon.getIconHeight();

                double scaleX   = (double) lbW / (double) imgW;
                double scaleY   = (double) lbH / (double) imgH;
                double scale    = Math.min(scaleX, scaleY);

                Image scaleImg = imgIcon.getImage().getScaledInstance((int) (scale * imgW), (int) (scale * imgH),Image.SCALE_SMOOTH);

                
                lb_img.setImagen(new ImageIcon(scaleImg));
            }  catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }
    
    private boolean validasiIn(){
        boolean valid = false;
        if(tf_id.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID tidak boleh kosong");
        } else if (tf_nama.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong");
        } else if (tf_username.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Username tidak boleh kosong");
        } else if (pf_pp.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Pasword tidak boleh kosong");
        } else if (!pf_pp.getText().trim().equals(pf_pp2.getText().trim())){
            JOptionPane.showMessageDialog(null, "Pasword tidak sama");
        } else if (tf_alamat.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Alamat tidak boleh kosong");
        } else if (cb_level.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Level tidak boleh kosong");
        } else {
            valid = true;
        }
        return valid;
    }
